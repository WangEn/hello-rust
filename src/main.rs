    // 遍历字符
    for c in "中国人".chars() {
        println!("{}", c);
    }

    // 遍历字节
    for b in "中国人".bytes() {
        println!("{}", b);
    }

    // 获取子串
    // 标准库是无法从“holla中国人नमस्ते”这个变长的字符串中取出一个子串的
